<li>選擇適合的精油： 根據你的冥想目標，選擇適合的精油。例如，如果你希望平靜情緒，可以選擇薰衣草精油；如果你想提振心情，則可以選擇柑橘類精油。</li>
<li>深呼吸與專注： 在冥想過程中，深呼吸是非常重要的。透過深呼吸，我們可以帶入更多新鮮的空氣，同時也可以將精油的芳香引入體內。專注於呼吸，感受每一次呼吸的流動。</li>
